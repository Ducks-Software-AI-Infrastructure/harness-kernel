const routesBySpecificity = [...moduleRoutes].sort((a, b) => b.source.length - a.source.length);
const moduleLabelBySourceIndex = new Map(moduleRoutes.map((route) => [`${route.source}/index.md`, route.label]));
const requiredLabels = moduleRoutes.map((route) => route.label);

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/gu, "\\$&");
}

function publicPathFor(relPath) {
  if (relPath === "index.md") return relPath;

  for (const route of routesBySpecificity) {
    if (relPath === `${route.source}/index.md`) {
      return `${route.route}/index.md`;
    }

    if (relPath.startsWith(`${route.source}/`)) {
      return `${route.route}/${relPath.slice(route.source.length + 1)}`;
    }
  }

  return relPath;
}

function titleFromPaths(sourceRelPath, publicRelPath) {
  if (publicRelPath === "index.md") return "API Reference";

  const moduleLabel = moduleLabelBySourceIndex.get(sourceRelPath);
  if (moduleLabel) return moduleLabel;

  return pathPosix
    .basename(publicRelPath, ".md")
    .replace(/^_+/u, "")
    .replace(/[-_]+/gu, " ")
    .replace(/\b\w/gu, (match) => match.toUpperCase());
}

function stripGeneratedMetadata(raw) {
  return raw
    .replace(/^---\n[\s\S]*?\n---\n\n?/u, "")
    .replace(/^> Generated by TypeDoc\. Do not edit this page directly\.\n\n/u, "");
}

function replaceModuleLabels(content) {
  let updated = content
    .replace(/\*\*harness-kernel-monorepo\*\*/gu, "**API Reference**")
    .replace(/^# harness-kernel-monorepo$/gmu, "# API Reference");

  for (const route of routesBySpecificity) {
    const source = escapeRegExp(route.source);
    updated = updated
      .replace(new RegExp(`\\[${source}\\]`, "gu"), `[${route.label}]`)
      .replace(new RegExp(`^(#+) ${source}$`, "gmu"), `$1 ${route.label}`)
      .replace(new RegExp(`\\*\\*${source}\\*\\*`, "gu"), `**${route.label}**`);
  }

  return updated;
}

function stripTrailingWhitespace(content) {
  return content.replace(/[ \t]+$/gmu, "");
}

function rewriteLinks(content, sourceRelPath, publicRelPath, publicPathBySourcePath) {
  return content.replace(/\]\(([^)\s]+\.md(?:#[^)]+)?)\)/gu, (match, target) => {
    if (/^(?:https?:|mailto:|\/|#)/u.test(target)) return match;

    const hashIndex = target.indexOf("#");
    const targetPath = hashIndex === -1 ? target : target.slice(0, hashIndex);
    const anchor = hashIndex === -1 ? "" : target.slice(hashIndex);
    const normalizedTarget = pathPosix.normalize(pathPosix.join(pathPosix.dirname(sourceRelPath), targetPath));
    const publicTarget = publicPathBySourcePath.get(normalizedTarget);

    if (!publicTarget) return match;

    let rewrittenTarget = pathPosix.relative(pathPosix.dirname(publicRelPath), publicTarget);
    if (!rewrittenTarget) rewrittenTarget = pathPosix.basename(publicTarget);

    return `](${rewrittenTarget}${anchor})`;
  });
}

function frontmatterFor(title) {
  return [
    "---",
    `title: ${JSON.stringify(title)}`,
    `description: ${JSON.stringify(`Generated API reference for ${title}.`)}`,
    "---",
    "",
  ].join("\n");
}

async function walk(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const path = join(dir, entry.name);
    if (entry.isDirectory()) files.push(...await walk(path));
    else if (entry.name.endsWith(".md")) files.push(path);
  }
  return files;
}

const generatedFiles = await walk(rawDir);
const publicPathBySourcePath = new Map();
const fileRecords = [];

for (const file of generatedFiles) {
  const sourceRelPath = relative(rawDir, file).replace(/\\/gu, "/");
  const publicRelPath = publicPathFor(sourceRelPath);
  publicPathBySourcePath.set(sourceRelPath, publicRelPath);
  fileRecords.push({
    sourceRelPath,
    publicRelPath,
    raw: await readFile(file, "utf8"),
  });
}

await rm(processedDir, { recursive: true, force: true });
await mkdir(processedDir, { recursive: true });

for (const record of fileRecords) {
  const title = titleFromPaths(record.sourceRelPath, record.publicRelPath);
  const withoutGenerated = stripGeneratedMetadata(record.raw);
  const rewritten = rewriteLinks(
    replaceModuleLabels(withoutGenerated),
    record.sourceRelPath,
    record.publicRelPath,
    publicPathBySourcePath,
  );
  const outPath = join(processedDir, record.publicRelPath);

  await mkdir(dirname(outPath), { recursive: true });
  await writeFile(outPath, stripTrailingWhitespace(`${frontmatterFor(title)}${notice}${rewritten}`), "utf8");
}

const rootIndex = await readFile(join(processedDir, "index.md"), "utf8");
for (const label of requiredLabels) {
  if (!rootIndex.includes(`[${label}](`)) {
    throw new Error(`Missing generated API reference group: ${label}`);
  }
}

await rm(referenceDir, { recursive: true, force: true });
await rename(processedDir, referenceDir);
